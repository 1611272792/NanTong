function GetAll() {
    var che = document.getElementById("checkAll");
    var ischeck = che.checked;
    if (che.checked == ischeck) {
        var a = document.getElementsByName("checkAll");
        for (var i = 0; i < a.length; i++) {
            a[i].checked = ischeck;
            // alert(a[i].value);//得到value值
        }
    }
}